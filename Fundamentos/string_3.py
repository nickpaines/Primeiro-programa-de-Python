nome = "Nicolas Paines"

print(nome[0])
print(nome[:7]) # pegando os 7 primeiros caracteres do nome
print(nome[8])
print(nome[5:]) # Pegando todos os caracteres a partir da posição 5 até o final
print(nome[7:10])  # Pegando apenas os 4 proximos caracteres (de 7 em 7)
print(nome[5:12:3]) # Pegando de  5 em 5 e saltando de  3 em 3, ou seja, vai pegar os 9 primeiros numeros.
print(nome[:])  # Copiando todo o conteudo da variavel para dentro de outra
print(nome[::-1])  # Inverter o nome

